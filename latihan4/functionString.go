//? function untuk string
/*
	1. len("string")	 => mengambil jumlah karakter di string
	2. "string"[number]  => mengambil posisi pada posisi yang di tentukan
*/

package main
import "fmt"

func main(){
	fmt.Println(len("Bintang")); // mengambil jumlah karakter
	fmt.Println("Muh Bintang Ramli"[0]); // mengambil karakter pada i 0
	fmt.Println("Muh Bintang Ramli"[1]); // mengambil karakter pada i 1
}